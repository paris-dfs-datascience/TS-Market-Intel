# Maps segment_raw values from SalesForce.Account_base to SF-aligned vertical names.
# Used as fallback when a company isn't found in ACCOUNTS (ACCOUNTS takes precedence).
SEGMENT_RAW_MAP: dict[str, str] = {
    "PHARMA-BIOTECH":           "BioPharma",
    "EDUCATION":                "Education & Research",
    "CLINICAL":                 "Clinical / Molecular Diagnostics",
    "MOLECULAR DIAGNOSTICS":    "Clinical / Molecular Diagnostics",
    "INDUSTRIAL":               "Industrial",
    "STATE & LOCAL GOVERNMENT": "Government",
    "FEDERAL GOV'T":            "Government",
    "RESELLERS":                "Industrial",       # default for unknown resellers
    "ADVANCED TECHNOLOGY":      "Industrial",       # default for unknown advanced tech
    "INTERNATIONAL":            "BioPharma",        # default for unknown international
}


def load_accounts_from_csv(filepath: str) -> dict[str, list[dict]]:
    """Load accounts from a CSV export of SalesForce.Account_base.

    Filters rows where tier IN ('Customer80', 'Super80'), maps segment_raw
    via SEGMENT_RAW_MAP, and returns a dict of:
        { vertical: [{"name": <Corporate_ID__c>, "parent_id": <ParentId>}, ...] }

    Expected CSV columns: Corporate_ID__c, ParentId, segment_raw, tier
    Rows with unknown segment_raw values are skipped with a printed warning.
    Handles Excel-generated BOM via utf-8-sig encoding.
    Deduplicates by Corporate_ID__c within each vertical (primary segment wins).
    """
    import csv as _csv
    from collections import defaultdict as _dd

    corp_data: dict = _dd(lambda: {"segments": _dd(int), "parent_id": None})
    with open(filepath, newline="", encoding="utf-8-sig") as f:
        reader = _csv.DictReader(f)
        for row in reader:
            tier = row.get("tier", "").strip()
            if tier not in ("Customer80", "Super80"):
                continue
            name = row.get("Corporate_ID__c", "").strip()
            if not name or name in ("0", "NULL"):
                continue
            segment = row.get("segment_raw", "").strip().upper()
            pid = row.get("ParentId", "").strip() or None
            corp_data[name]["segments"][segment] += 1
            if corp_data[name]["parent_id"] is None and pid and pid != "NULL":
                corp_data[name]["parent_id"] = pid

    # Build ACCOUNTS lookup so manually curated verticals take precedence over segment_raw heuristic
    acct_vertical_lookup = {acct.upper(): cat for cat, accts in ACCOUNTS.items() for acct in accts}

    result: dict[str, list[dict]] = {}
    skipped_segments: set[str] = set()
    for name, info in corp_data.items():
        # Prefer manual classification from ACCOUNTS dict
        vertical = acct_vertical_lookup.get(name.upper())
        if vertical is None:
            primary_seg = max(info["segments"], key=lambda s: (info["segments"][s], s))
            vertical = SEGMENT_RAW_MAP.get(primary_seg)
        if vertical is None:
            primary_seg = max(info["segments"], key=lambda s: (info["segments"][s], s))
            skipped_segments.add(primary_seg)
            continue
        result.setdefault(vertical, []).append({"name": name, "parent_id": info["parent_id"]})

    if skipped_segments:
        print(
            f"WARNING: load_accounts_from_csv skipped rows with unmapped segment_raw values: "
            f"{sorted(skipped_segments)}. Add them to SEGMENT_RAW_MAP in accounts.py to include them."
        )

    return result
